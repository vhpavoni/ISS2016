    private void fecharVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fecharVendaActionPerformed
        if(!produtoVenda.getItens().isEmpty()){
            if(comboCliente.getSelectedItem() != null){
                Cliente cli = GerenciarCliente.buscarCliente((String)comboCliente.getSelectedItem());
                if (GerenciarVenda.finalizarVenda (cli.getLogin(), produtoVenda)){
                    JOptionPane.showMessageDialog(null, "Venda Realizada com sucesso!");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "N√£o foi possivel Realizar a venda");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Nenhum produto incluido na venda");
        }
    }//GEN-LAST:event_fecharVendaActionPerformed

    private void excluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirProdutoActionPerformed
        if (tabela.getSelectedRow() != -1){
            int linha = tabela.getSelectedRow();
            int i;
            if (tabela.getValueAt(linha, 0) != null ){
                produtoVenda.getItens().remove(produtoVenda.getIten((Long)tabela.getValueAt(linha, 0)));
                for (i=linha; i<tabela.getRowCount()-1; i++){
                    tabela.setValueAt(tabela.getValueAt(i+1, 0), i, 0);
                    tabela.setValueAt(tabela.getValueAt(i+1, 1), i, 1);
                    tabela.setValueAt(tabela.getValueAt(i+1, 2), i, 2);
                    tabela.setValueAt(tabela.getValueAt(i+1, 3), i, 3);
                    tabela.setValueAt(tabela.getValueAt(i+1, 4), i, 4);
                    tabela.setValueAt(tabela.getValueAt(i+1, 5), i, 5);
                }
                tabela.setValueAt(null, i, 0);
                tabela.setValueAt(null, i, 1);
                tabela.setValueAt(null, i, 2);
                tabela.setValueAt(null, i, 3);
                tabela.setValueAt(null, i, 4);
                tabela.setValueAt(null, i, 5);
                if(i>9){
                    DefaultTableModel tableModel = (DefaultTableModel)tabela.getModel();
                    tableModel.setNumRows(tabela.getRowCount()-1);
                }
            }
        }
    }//GEN-LAST:event_excluirProdutoActionPerformed

